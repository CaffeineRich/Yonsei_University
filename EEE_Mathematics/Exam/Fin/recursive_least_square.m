A0=[1 0;
    0 1;
    1 1];
A1=[2 1];
A2=[3 1];

b0=[1;1;1];
b1=[3];
b2=[4];

G0=A0.'*A0;
G1=G0+A1.'*A1;
% x1=inv(G1)*[A0;A1].'*[b0;b1];

P0=inv(G0);
P1=P0-(P0*A1.'*A1*P0)/(1+A1*P0*A1.');
P2=P1-(P1*A2.'*A2*P1)/(1+A2*P1*A2.');

x0=P0*A0.'*b0;
x1=x0+P1*A1.'*(b1-A1*x0);
x2=x1+P2*A2.'*(b2-A2*x1);

disp(P0);
disp(P1);
disp(P2);
disp(x0);
disp(x1);
disp(x2);
